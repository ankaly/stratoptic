_ILLUMINANTS = {'D65': _D65, 'A': _ILL_A}


def spectrum_to_XYZ(wavelengths, spectrum, illuminant='D65'):
    """
    R or T spectrum (0–1) → CIE XYZ tristimulus values.

    wavelengths : nm array (arbitrary spacing, will be interpolated)
    spectrum    : 0–1 array, same length as wavelengths
    illuminant  : 'D65' or 'A'
    """
    spd = _ILLUMINANTS.get(illuminant, _D65)

    # Interpolate spectrum onto 5 nm CIE grid
    s = np.interp(_WL, wavelengths, spectrum, left=0.0, right=0.0)

    k = 100.0 / np.sum(spd * _Y_BAR)
    X = k * np.sum(spd * s * _X_BAR)
    Y = k * np.sum(spd * s * _Y_BAR)
    Z = k * np.sum(spd * s * _Z_BAR)
    return float(X), float(Y), float(Z)


def XYZ_to_xy(X, Y, Z):
    """CIE 1931 chromaticity coordinates."""
    denom = X + Y + Z
    if denom < 1e-10:
        return 0.3127, 0.3290  # D65 white point fallback
    return X / denom, Y / denom


def XYZ_to_sRGB(X, Y, Z):
    """
    XYZ (D65, Y normalised to 100) → sRGB (0–255, gamma corrected).
    """
    # Normalise Y to 1
    x, y, z = X / 100.0, Y / 100.0, Z / 100.0

    # Linear sRGB (D65)
    r_lin =  3.2406 * x - 1.5372 * y - 0.4986 * z
    g_lin = -0.9689 * x + 1.8758 * y + 0.0415 * z
    b_lin =  0.0557 * x - 0.2040 * y + 1.0570 * z

    # Gamma correction
    def _gamma(v):
        v = np.clip(v, 0.0, 1.0)
        return np.where(v <= 0.0031308,
                        12.92 * v,
                        1.055 * np.power(v, 1.0 / 2.4) - 0.055)

    R = int(np.clip(round(_gamma(r_lin) * 255), 0, 255))
    G = int(np.clip(round(_gamma(g_lin) * 255), 0, 255))
    B = int(np.clip(round(_gamma(b_lin) * 255), 0, 255))
    return R, G, B


def coating_color(result, mode='reflection', illuminant='D65'):
    """
    TMMResult → perceived color dict.

    mode : 'reflection' uses R, 'transmission' uses T
    Returns {'xy', 'XYZ', 'sRGB', 'hex'}
    """
    spectrum = result.R if mode == 'reflection' else result.T
    X, Y, Z = spectrum_to_XYZ(result.wavelengths, spectrum, illuminant)
    xy = XYZ_to_xy(X, Y, Z)
    rgb = XYZ_to_sRGB(X, Y, Z)
    hex_color = '#{:02X}{:02X}{:02X}'.format(*rgb)
    return {
        'xy'  : xy,
        'XYZ' : (X, Y, Z),
        'sRGB': rgb,
        'hex' : hex_color,
    }
